package findMe.domain;
/**
 * Auxiliar class to be used in DAO manipulations, within selects
 * @author Rebeca Galdino and Vini Farias 
 *
 */
public class AuxiliarObject {
	
	private String a;
	private String b;
	private String c;
	private int d;
	private int e;
	private double f;
	private double g;
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	public int getE() {
		return e;
	}
	public void setE(int e) {
		this.e = e;
	}
	public double getF() {
		return f;
	}
	public void setF(double f) {
		this.f = f;
	}
	public double getG() {
		return g;
	}
	public void setG(double g) {
		this.g = g;
	}
	
	

}
